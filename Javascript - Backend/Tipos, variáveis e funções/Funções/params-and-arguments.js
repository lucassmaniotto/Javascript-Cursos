function sum(x , y) {
    return x + y
}
function diff(x , y) {
    return x - y
}
function mult(x , y) {
    return x * y
}
function div(x , y) {
    return x / y
}

console.log(sum(40, 2));
console.log(diff(44, 2));
console.log(div(84, 2));
console.log(mult(21, 2));

